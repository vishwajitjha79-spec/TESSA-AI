export function estimateCalories(food: string): FoodItem {
  const normalized = food.toLowerCase().trim();
  
  // Direct match
  if (FOOD_DATABASE[normalized]) {
    return {
      food: food,
      calories: FOOD_DATABASE[normalized],
      confidence: 'high',
    };
  }
  
  // Partial match
  for (const [key, cal] of Object.entries(FOOD_DATABASE)) {
    if (normalized.includes(key) || key.includes(normalized)) {
      return {
        food: food,
        calories: cal,
        confidence: 'medium',
      };
    }
  }
  
  // Default estimate for unknown foods
  return {
    food: food,
    calories: 150,
    confidence: 'low',
  };
}
